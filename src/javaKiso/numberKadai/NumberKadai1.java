package javaKiso.numberKadai;

public class NumberKadai1 {

	public static void main(String[] args) {
		int numberA = 2;
		int numberB = 3;
		int numberC = 10;
		int numberD = 29;

		System.out.println("a.の結果 = " + numberA * numberB);
		System.out.println("b.の結果 = " + numberC / numberA);
		System.out.println("c.の結果 = " + numberC / numberB);
		System.out.println("d.の結果 = " + numberD % numberB);
	}

}
