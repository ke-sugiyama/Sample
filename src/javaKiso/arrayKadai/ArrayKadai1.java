package javaKiso.arrayKadai;

public class ArrayKadai1 {

	public static void main(String[] args) {
		String[] stringArray = new String[6];
		stringArray[0] = "ライオンズ";
		stringArray[1] = "ホークス";
		stringArray[2] = "イーグルス";
		stringArray[3] = "マリーンズ";
		stringArray[4] = "ファイターズ";
		stringArray[5] = "バファローズ";

		System.out.println("1個目：" + stringArray[0]);
		System.out.println("2個目：" + stringArray[1]);
		System.out.println("3個目：" + stringArray[2]);
		System.out.println("4個目：" + stringArray[3]);
		System.out.println("5個目：" + stringArray[4]);
		System.out.println("6個目：" + stringArray[5]);

	}

}
